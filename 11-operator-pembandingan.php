<?php

$var1 = 10;
$var2 = 20;

// sama dengan identik dengan tipe data
var_dump($var1 === $var2);  // false

//tidak sama dengan
var_dump($var1 !== $var2); // tidak sama dengan 

//coba identik tidak sama dengan
var_dump($var1 !== $var2);

//kurang dari
var_dump($var1 < $var2); 

//lebih dari
var_dump($var1 > $var2);



